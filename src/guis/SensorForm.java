    public void agregar(){
        String marca = this.txtMarca.getText();
        String fabricante = this.txtFabricante.getText();
        String formatoTransmision = this.txtFormato.getText();
        
        Sensor sensorNuevo = new Sensor(formatoTransmision, marca, fabricante);
        boolean seAgregoSensor = this.sensorDAO.agregar(sensorNuevo);
        
        if(seAgregoSensor){
            JOptionPane.showMessageDialog(this, "Se registr贸 el nuevo sensor.", "Informaci贸n", JOptionPane.INFORMATION_MESSAGE);
            this.limpiarFormulario();
        }else{
            JOptionPane.showMessageDialog(this, "No fue posible registrar el sensor.", "Informaci贸n", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void limpiarFormulario(){
        this.txtFabricante.setText("");
        this.txtFormato.setText("");
        this.txtMarca.setText("");
    }

